function changeColor() 
/*gör så att när man trycker på detta element så byter den färgen på texten till vit*/
{
    var element = document.getElementById("changeColorElement");
    element.style.color = "#FFFFFF"; 
}

function changeBackgroundColor()
/*gör så att när man trycker på detta element så byter den färgen på bakgrunden till svart*/
{
    var element = document.getElementById("changeBackgroundColorElement");
    element.style.backgroundColor = "#000000"
}